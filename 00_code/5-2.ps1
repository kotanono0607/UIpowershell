
function 5_2 {
    # 入力方法を選択
    $選択肢 = @("URLを直接入力", "ファイルを選択")
    $選択結果 = リストから項目を選択 -フォームタイトル "URLを開く" -ラベルテキスト "入力方法を選択してください:" -選択肢リスト $選択肢

    if (-not $選択結果) {
        return "# キャンセルされました"
    }

    $URL = $null

    if ($選択結果 -eq "URLを直接入力") {
        # URLを直接入力
        $URL = 文字列を入力 -フォームタイトル "URLを開く" -ラベルテキスト "開くURLを入力してください:"
    } else {
        # ファイルを選択
        $URL = ファイルを選択 -タイトル "開くファイルを選択" -フィルタ "HTML Files (*.html;*.htm)|*.html;*.htm|All Files (*.*)|*.*"
    }

    # 入力された値を使ってコードを生成
    if ($URL) {
        return "URLを開く -URL `"$URL`""
    } else {
        return "# キャンセルされました"
    }
}
